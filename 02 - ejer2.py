"""
Dos números enteros, multiplicarlo y mostrarlo
"""
num1 = int(input("Inserta número 1: "))
num2 = int(input("Inserta número 2: "))
mult = num1 * num2
print("El resultado de la multiplicación es: ")
print(mult)

"""
Dos números reales, restar y mostrarlo
"""
num1 = float(input("Inserta número 1: "))
num2 = float(input("Inserta número 2: "))
rest = num1 - num2
print("El resultado de la resta es: ")
print(rest)
